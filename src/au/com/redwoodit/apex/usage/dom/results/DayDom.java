package au.com.redwoodit.apex.usage.dom.results;

public class DayDom
{
	
	private String whatDay; 
	private String nometer;
	private String ingress;
	private String egress;

	public String getNometer()
	{
		return nometer;
	}

	public void setNometer(String nometer)
	{
		this.nometer = nometer;
	}

	public String getIngress()
	{
		return ingress;
	}

	public void setIngress(String ingress)
	{
		this.ingress = ingress;
	}

	public String getEgress()
	{
		return egress;
	}

	public void setEgress(String egress)
	{
		this.egress = egress;
	}

	public String getWhatDay()
	{
		return whatDay;
	}

	public void setWhatDay(String whatDay)
	{
		this.whatDay = whatDay;
	}

}
